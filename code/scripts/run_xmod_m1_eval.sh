#!/usr/bin/env bash
set -euo pipefail

ROUTE_ID_OR_XML="${1:-24330}"
CKPT="${2:-/mnt/c/xmod_b2d/m1/checkpoints/xmod_m1_shard_20260618_v2_best.pt}"
TAG="${3:-m1_candidate}"
BASE="/mnt/c/xmod_b2d/m1"
RUN_TAG="$(date +%Y%m%d_%H%M%S)"

if [[ "$ROUTE_ID_OR_XML" == *.xml ]]; then
  ROUTE_XML="$ROUTE_ID_OR_XML"
  ROUTE_ID="$(basename "$ROUTE_ID_OR_XML" .xml)"
else
  ROUTE_ID="$ROUTE_ID_OR_XML"
  ROUTE_XML="/mnt/c/pdmlite_eval/routes/${ROUTE_ID}.xml"
fi

export CARLA_ROOT="/mnt/c/CARLA_0.9.15/WindowsNoEditor"
export WORK_DIR="/mnt/c/carla_garage/Bench2Drive"
export SCENARIO_RUNNER_ROOT="$WORK_DIR/scenario_runner"
export LEADERBOARD_ROOT="$WORK_DIR/leaderboard"
export PYTHONPATH="/mnt/c/xmod_b2d:$CARLA_ROOT/PythonAPI/carla:$SCENARIO_RUNNER_ROOT:$LEADERBOARD_ROOT"
export IS_BENCH2DRIVE=True
export EXTERNAL_CARLA=1
export XMOD_CKPT="$CKPT"
unset SAVE_PATH
unset DATAGEN

PY="/home/lloitesa/miniconda3/envs/lead/bin/python"
OUT="$BASE/eval/${TAG}_${ROUTE_ID}_${RUN_TAG}_endpoint.json"
LOG="$BASE/logs/eval_${TAG}_${ROUTE_ID}_${RUN_TAG}.log"
LEADERBOARD_TIMEOUT="${LEADERBOARD_TIMEOUT:-180}"
OUTER_TIMEOUT="${OUTER_TIMEOUT:-500}"
export XMOD_TRACE_DIR="${XMOD_TRACE_DIR:-$BASE/traces/${TAG}_${ROUTE_ID}_${RUN_TAG}}"

mkdir -p "$BASE/eval" "$BASE/logs"
mkdir -p "$XMOD_TRACE_DIR"
cd "$WORK_DIR"

{
  echo "ROUTE_ID=$ROUTE_ID"
  echo "ROUTES=$ROUTE_XML"
  echo "CKPT=$CKPT"
  echo "OUT=$OUT"
  echo "LEADERBOARD_TIMEOUT=$LEADERBOARD_TIMEOUT"
  echo "OUTER_TIMEOUT=$OUTER_TIMEOUT"
  echo "XMOD_SAFETY_SHIELD=${XMOD_SAFETY_SHIELD:-0}"
  echo "XMOD_RECOVERY_SHIELD=${XMOD_RECOVERY_SHIELD:-0}"
  echo "XMOD_EGO_MODE=${XMOD_EGO_MODE:-auto}"
  echo "XMOD_MODEL_ARCH=${XMOD_MODEL_ARCH:-auto}"
  echo "XMOD_SAFETY_DISTANCE=${XMOD_SAFETY_DISTANCE:-9.0}"
  echo "XMOD_HARD_BRAKE_DISTANCE=${XMOD_HARD_BRAKE_DISTANCE:-5.5}"
  echo "XMOD_RECOVERY_AFTER=${XMOD_RECOVERY_AFTER:-35}"
  echo "XMOD_TRACE_DIR=${XMOD_TRACE_DIR:-}"
  echo "XMOD_TRACE_EVERY=${XMOD_TRACE_EVERY:-25}"
  echo "XMOD_TRACE_STUCK_AFTER=${XMOD_TRACE_STUCK_AFTER:-120}"
  echo "XMOD_TRACE_HAZARD_DISTANCE=${XMOD_TRACE_HAZARD_DISTANCE:-10.0}"
  echo "XMOD_TRACE_HAZARD_EVERY=${XMOD_TRACE_HAZARD_EVERY:-5}"
  echo "XMOD_TRACE_LIMIT=${XMOD_TRACE_LIMIT:-240}"
  echo "XMOD_TRACE_SAVE_IMAGES=${XMOD_TRACE_SAVE_IMAGES:-1}"
  echo "XMOD_INTERACTION_ENABLE=${XMOD_INTERACTION_ENABLE:-0}"
  echo "XMOD_INTERACTION_CKPT=${XMOD_INTERACTION_CKPT:-}"
  echo "XMOD_INTERACTION_MIN_DISTANCE=${XMOD_INTERACTION_MIN_DISTANCE:-0.0}"
  echo "XMOD_INTERACTION_MAX_DISTANCE=${XMOD_INTERACTION_MAX_DISTANCE:-22.0}"
  echo "XMOD_INTERACTION_BLEND=${XMOD_INTERACTION_BLEND:-0.55}"
  echo "XMOD_INTERACTION_VEHICLE_ONLY=${XMOD_INTERACTION_VEHICLE_ONLY:-1}"
  echo "XMOD_INTERACTION_MAX_BRAKE=${XMOD_INTERACTION_MAX_BRAKE:-0.75}"
  echo "XMOD_INTERACTION_MIN_THROTTLE=${XMOD_INTERACTION_MIN_THROTTLE:-0.0}"
  echo "XMOD_FRONT_RECOVERY_ENABLE=${XMOD_FRONT_RECOVERY_ENABLE:-0}"
  echo "XMOD_FRONT_RECOVERY_AFTER=${XMOD_FRONT_RECOVERY_AFTER:-90}"
  echo "XMOD_FRONT_RECOVERY_DISTANCE=${XMOD_FRONT_RECOVERY_DISTANCE:-6.5}"
  echo "XMOD_FRONT_RECOVERY_REVERSE_STEPS=${XMOD_FRONT_RECOVERY_REVERSE_STEPS:-28}"
  echo "XMOD_FRONT_RECOVERY_FORWARD_STEPS=${XMOD_FRONT_RECOVERY_FORWARD_STEPS:-45}"
  echo "XMOD_FRONT_RECOVERY_REVERSE_THROTTLE=${XMOD_FRONT_RECOVERY_REVERSE_THROTTLE:-0.45}"
  echo "XMOD_FRONT_RECOVERY_FORWARD_THROTTLE=${XMOD_FRONT_RECOVERY_FORWARD_THROTTLE:-0.70}"
  echo "XMOD_FRONT_RECOVERY_STEER=${XMOD_FRONT_RECOVERY_STEER:-0.70}"
  echo "XMOD_SOURCE_BRAKE_ENABLE=${XMOD_SOURCE_BRAKE_ENABLE:-0}"
  echo "XMOD_SOURCE_BRAKE_CONTROL=${XMOD_SOURCE_BRAKE_CONTROL:-0}"
  echo "XMOD_SOURCE_BRAKE_CKPT=${XMOD_SOURCE_BRAKE_CKPT:-}"
  echo "XMOD_SOURCE_BRAKE_MODE=${XMOD_SOURCE_BRAKE_MODE:-object}"
  echo "XMOD_SOURCE_BRAKE_NEAR_THRESHOLD=${XMOD_SOURCE_BRAKE_NEAR_THRESHOLD:-0.90}"
  echo "XMOD_SOURCE_BRAKE_OBJECT_THRESHOLD=${XMOD_SOURCE_BRAKE_OBJECT_THRESHOLD:-0.85}"
  echo "XMOD_SOURCE_BRAKE_ROUTE_THRESHOLD=${XMOD_SOURCE_BRAKE_ROUTE_THRESHOLD:-0.95}"
  echo "XMOD_SOURCE_BRAKE_BRAKE=${XMOD_SOURCE_BRAKE_BRAKE:-0.55}"
  echo "XMOD_SOURCE_BRAKE_MAX_THROTTLE=${XMOD_SOURCE_BRAKE_MAX_THROTTLE:-0.15}"
  echo "START_UTC=$(date -u +%Y-%m-%dT%H:%M:%SZ)"
} | tee "$LOG"

set +e
timeout "$OUTER_TIMEOUT" "$PY" leaderboard/leaderboard/leaderboard_evaluator.py \
  --routes="$ROUTE_XML" \
  --repetitions=1 \
  --track=SENSORS \
  --checkpoint="$OUT" \
  --agent=/mnt/c/xmod_b2d/xmod_b2d_agent.py \
  --agent-config="" \
  --debug=0 \
  --resume=False \
  --port=2000 \
  --traffic-manager-port=2500 \
  --traffic-manager-seed=${XMOD_TM_SEED:-0} \
  --gpu-rank=0 \
  --timeout="$LEADERBOARD_TIMEOUT" \
  >> "$LOG" 2>&1
status=$?
set -e

{
  echo "EVALUATOR_EXIT=$status"
  echo "END_UTC=$(date -u +%Y-%m-%dT%H:%M:%SZ)"
  echo "LOG=$LOG"
} | tee -a "$LOG"

tail -n 160 "$LOG"
exit "$status"
